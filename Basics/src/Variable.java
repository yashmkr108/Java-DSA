public class Variable {
    static void main() {

        // define and initialization
        int age = 20;

        // reassign
        age = 45;
        System.out.println(age);

        // case sensitive
        int weight = 21;
        int WEIGHT = 31;

        // starting letter
        int marks = 72;
        int _marks = 72;
        int Marks = 72;
        int $Marks = 72;

        // subsequent letter
        int height = 6;
        int height_ = 6;
        int height_5 = 6;
        int height$ = 6;

        // reserved keyword
           //int class = 21;

        // name should be meaningful

        // constant -> name convension of constant is uppercase
        int DAYS_IN_YEAR = 365;
        DAYS_IN_YEAR = 366;

        // camel case
        int myNameIsYash = 46;
    }
}